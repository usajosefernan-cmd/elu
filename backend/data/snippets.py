def map_value_to_level(value: int) -> str:
    if value == 0: return 'OFF'
    if 1 <= value <= 3: return 'LOW'
    if 4 <= value <= 6: return 'MED'
    if 7 <= value <= 9: return 'HIGH'
    if value == 10: return 'FORCE'
    return 'OFF'

def get_default_pillars_config():
    # Helper para generar la estructura inicial
    
    pillars = ['photoscaler', 'stylescaler', 'lightscaler']
    defaults = {
        'photoscaler': ['limpieza_artefactos', 'geometria', 'optica', 'chronos', 'senal_raw', 'sintesis_adn', 'grano_filmico', 'enfoque', 'resolucion'],
        'stylescaler': ['styling_piel', 'styling_pelo', 'styling_ropa', 'maquillaje', 'limpieza_entorno', 'reencuadre_ia', 'atmosfera', 'look_cine', 'materiales_pbr'],
        'lightscaler': ['key_light', 'fill_light', 'rim_light', 'volumetria', 'temperatura', 'contraste', 'sombras', 'estilo_autor', 'reflejos']
    }
    
    config = {}
    for p in pillars:
        sliders = []
        for s_name in defaults[p]:
            val = 5
            if s_name == 'grano_filmico' or s_name == 'maquillaje' or s_name == 'atmosfera' or s_name == 'materiales_pbr' or s_name == 'rim_light' or s_name == 'volumetria' or s_name == 'reflejos':
                val = 4
            
            sliders.append({
                "name": s_name,
                "value": val,
                "levelText": map_value_to_level(val),
                "snippet": SNIPPET_DICTIONARY[p][s_name][val]
            })
        
        config[p] = {
            "pillarName": p,
            "mode": "auto",
            "sliders": sliders
        }
    return config
